
// ============================================================================
// WebSocket сервер на базе uWebSockets с поддержтой UTF-8
// Рассылка сообщений всем подключенным клиентам
// ============================================================================

using UserData = std::nullptr_t;

// ----------------------------------------------------------------------------
// Глобальные данные для управления подключениями
// ----------------------------------------------------------------------------

// Хранилище указателей на активные WebSocket соединения
std::vector<uWS::WebSocket<false, true, UserData>*> activeSockets;

// Мьютекс для потокобезопасного доступа к списку сокетов
std::mutex socketsMutex;

/**
 * Отправка сообщения всем подключенным клиентам
 * @param message - сообщение для рассылки
 * @param opCode - тип сообщения (TEXT или BINARY)
 */
void broadcastMessage(std::string_view message, uWS::OpCode opCode) {
    // Блокируем мьютекс для безопасного доступа к списку
    std::lock_guard<std::mutex> lock(socketsMutex);
    
    // Отправляем сообщение всем сокетам в списке
    for (auto* ws : activeSockets) {
        if (ws) {
            // Пытаемся отправить сообщение
            ws->send(message, opCode);
        }
    }
}
